-- ==========================================
-- 1. SCHEMA DEFINITION
-- ==========================================

-- Enable the pgcrypto extension for gen_random_uuid()
CREATE EXTENSION IF NOT EXISTS "pgcrypto";

-- Independent Tables
CREATE TABLE bands (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE musicians (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    phone TEXT,
    email TEXT,
    bio TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE instruments (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE songs (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    artist TEXT NOT NULL,
    vocal_range TEXT,
    notes TEXT,
    link TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE setlists (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE master_setlists (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE tours (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tables with Foreign Keys
CREATE TABLE events (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    location TEXT NOT NULL,
    date DATE,
    time TIME,
    tour_id UUID REFERENCES tours(id) ON DELETE SET NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Junction Tables
CREATE TABLE band_musicians (
    band_id UUID REFERENCES bands(id) ON DELETE CASCADE,
    musician_id UUID REFERENCES musicians(id) ON DELETE CASCADE,
    PRIMARY KEY (band_id, musician_id)
);

CREATE TABLE musician_instruments (
    musician_id UUID REFERENCES musicians(id) ON DELETE CASCADE,
    instrument_id UUID REFERENCES instruments(id) ON DELETE CASCADE,
    PRIMARY KEY (musician_id, instrument_id)
);

CREATE TABLE song_vocalists (
    song_id UUID REFERENCES songs(id) ON DELETE CASCADE,
    musician_id UUID REFERENCES musicians(id) ON DELETE CASCADE,
    PRIMARY KEY (song_id, musician_id)
);

CREATE TABLE setlist_songs (
    setlist_id UUID REFERENCES setlists(id) ON DELETE CASCADE,
    song_id UUID REFERENCES songs(id) ON DELETE CASCADE,
    position INTEGER NOT NULL DEFAULT 0,
    PRIMARY KEY (setlist_id, song_id)
);

CREATE TABLE event_setlists (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_id UUID NOT NULL REFERENCES events(id) ON DELETE CASCADE,
    setlist_id UUID REFERENCES setlists(id) ON DELETE SET NULL,
    master_setlist_id UUID REFERENCES master_setlists(id) ON DELETE SET NULL,
    position INTEGER NOT NULL DEFAULT 0,
    CONSTRAINT check_setlist_type CHECK (
        (setlist_id IS NOT NULL AND master_setlist_id IS NULL) OR
        (setlist_id IS NULL AND master_setlist_id IS NOT NULL)
    )
);

CREATE TABLE master_setlist_setlists (
    master_setlist_id UUID REFERENCES master_setlists(id) ON DELETE CASCADE,
    setlist_id UUID REFERENCES setlists(id) ON DELETE CASCADE,
    position INTEGER NOT NULL DEFAULT 0,
    PRIMARY KEY (master_setlist_id, setlist_id)
);

